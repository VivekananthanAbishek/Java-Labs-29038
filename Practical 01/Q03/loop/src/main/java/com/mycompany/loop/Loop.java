
package com.mycompany.loop;
public class Loop 
{

    public static void main(String[] args) 
    {
        int i;
       for(i=0;i<5;i++)
       {
           System.out.println("Executing Loop "+i);
       }
    
    System.out.println("\n\n");
       int j=0;
       while(j<5)
       {
           System.out.println("Executing Loop "+j);
           j++;
       }
    }
    
}
